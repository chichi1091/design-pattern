package com.design_pattern.template_method;

public class CharDispay extends AbstractDisplay {
    private char ch;

    public CharDispay(char ch) {
        this.ch = ch;
    }

    @Override
    public void open() {
        System.out.print("<<");
    }

    @Override
    public void print() {
        System.out.print(ch);
    }

    @Override
    public void close() {
        System.out.println(">>");
    }
}
